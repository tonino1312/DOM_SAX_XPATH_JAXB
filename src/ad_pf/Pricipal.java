    private void BtnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAñadirActionPerformed
        // Obtener los valores de los campos de entrada
        String idNuevo = jTextFieldId.getText();
        String marcaNuevo = jTextFieldMarca.getText();
        String modeloNuevo = jTextFieldModelo.getText();
        String colorHexNuevo = jTextFieldColorHex.getText();
        String colorNuevo = jTextFieldColor.getText();
        String potenciaNuevo = jTextFieldPotencia.getText();
        String precioNuevo = jTextFieldPrecio.getText();
        String puertasNuevo = jTextFieldPuertas.getText();

        // Obtener los valores de los campos de entrada de extras
        String extra1 = jTextFieldExtra1.getText();
        String extra2 = jTextFieldExtra2.getText();
        String extra3 = jTextFieldExtra3.getText();
        String extra4 = jTextFieldExtra4.getText();
        String extra5 = jTextFieldExtra5.getText();

        // Crear la lista de extras
     List<String> extrasNuevo = new ArrayList<>();
        if (!extra1.isEmpty()) {
            extrasNuevo.add(extra1);
        }
        if (!extra2.isEmpty()) {
            extrasNuevo.add(extra2);
        }
        if (!extra3.isEmpty()) {
            extrasNuevo.add(extra3);
        }
        if (!extra4.isEmpty()) {
            extrasNuevo.add(extra4);
        }
        if (!extra5.isEmpty()) {
            extrasNuevo.add(extra5);
        }
        
        // Sumar 1000 al precioNuevo por cada extra
        int numExtras = extrasNuevo.size();
        int precioInt = Integer.parseInt(precioNuevo);
        int precioConExtras = precioInt + (numExtras * 1000);
        precioNuevo = String.valueOf(precioConExtras);

        // Llamar al método agregarCoche, pasando los valores de los campos de entrada, la ruta del archivo XML y la lista de extras nuevos
        dom.agregarCoche(ficheroSeleccionado, idNuevo, marcaNuevo, modeloNuevo, colorHexNuevo, colorNuevo, potenciaNuevo, precioNuevo, puertasNuevo, extrasNuevo);
        dom.guardar_XML_DOM(ficheroSeleccionado);
    }//GEN-LAST:event_BtnAñadirActionPerformed

