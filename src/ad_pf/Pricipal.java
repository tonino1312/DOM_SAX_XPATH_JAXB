    public File seleccionarArchivo() {
        // Creamos una instancia de JFileChooser que es un componente Swing que permite al usuario seleccionar un archivo
        JFileChooser chooser = new JFileChooser();
        // Creamos un filtro que solo permite archivos con la extensión .xml
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos XML", "xml");
        // Aplicamos el filtro al archivo escogido
        chooser.setFileFilter(filter);

        // Se espera a la respuesta del usuario
        // El método showOpenDialog devuelve un valor entero que indica si el usuario ha seleccionado un archivo o lo ha cancelado
        int returnVal = chooser.showOpenDialog(this);
        // Si el usuario ha elegido un archivo, se ejecutará este if
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            // Devolvemos el archivo seleccionado por el usuario
            return chooser.getSelectedFile();
        } else {
            return null;
        }
    }
    


