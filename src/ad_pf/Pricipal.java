    private void BtnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInfoActionPerformed
        // Obtener ID ingresado en jTextFieldId
        String id = jTextFieldId.getText();

        // Construir consulta XPath
        String consulta = "/coches/coche[@id='" + id + "']";

        // Buscar nodos que cumplen la condición
        NodeList nodos = dom.buscarNodosXPath(consulta);

        // Verificar si se encontró algún nodo
        if (nodos.getLength() > 0) {
            // Obtener valores de los nodos y setearlos en los JTextField correspondientes
            Element coche = (Element) nodos.item(0); // asumimos que sólo hay un coche con el ID dado
            jTextFieldMarca.setText(coche.getElementsByTagName("marca").item(0).getTextContent());
            jTextFieldModelo.setText(coche.getElementsByTagName("modelo").item(0).getTextContent());
            jTextFieldColor.setText(coche.getElementsByTagName("color").item(0).getTextContent());
            jTextFieldColorHex.setText(((Element) coche.getElementsByTagName("color").item(0)).getAttributeNode("hex").getValue());
            jTextFieldPotencia.setText(coche.getElementsByTagName("potencia").item(0).getTextContent());
            jTextFieldPrecio.setText(coche.getElementsByTagName("precio").item(0).getTextContent());
            jTextFieldPuertas.setText(coche.getElementsByTagName("puertas").item(0).getTextContent());
            NodeList extras = coche.getElementsByTagName("extra");
            for (int i = 0; i < extras.getLength(); i++) {
                Element extra = (Element) extras.item(i);
                switch (i) {
                    case 0:
                        jTextFieldExtra1.setText(extra.getTextContent());
                        break;
                    case 1:
                        jTextFieldExtra2.setText(extra.getTextContent());
                        break;
                    case 2:
                        jTextFieldExtra3.setText(extra.getTextContent());
                        break;
                    case 3:
                        jTextFieldExtra4.setText(extra.getTextContent());
                        break;
                    case 4:
                        jTextFieldExtra5.setText(extra.getTextContent());
                        break;
                    default:
                        break;
                }
            }
        } else {
            // No se encontró ningún coche con el ID dado
            JOptionPane.showMessageDialog(this, "No se encontró ningún coche con el ID " + id);
        }
    }//GEN-LAST:event_BtnInfoActionPerformed

